# ── Helper: F2 Score ───────────────────────────────────────────────────
def f2_score(y_true, y_pred_prob, threshold=0.5):
    y_pred = (y_pred_prob >= threshold).astype(int)
    return fbeta_score(y_true, y_pred, beta=2, average='binary')

def find_best_threshold(y_true, y_pred_prob, thresholds=np.arange(0.2, 0.7, 0.01)):
    """หา threshold ที่ให้ F2 สูงสุด"""
    best_t, best_score = 0.5, 0
    for t in thresholds:
        score = f2_score(y_true, y_pred_prob, t)
        if score > best_score:
            best_score = score
            best_t = t
    return best_t, best_score

