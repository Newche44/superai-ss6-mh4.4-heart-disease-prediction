def add_features(df):
    df = df.copy()

    # Risk score: รวม binary risk factors
    risk_cols = [
        'High Blood Pressure', 'Told High Cholesterol',
        'Diagnosed Stroke', 'Diagnosed Diabetes',
        'Smoked 100+ Cigarettes', 'Heavy Alcohol Consumption'
    ]
    available = [c for c in risk_cols if c in df.columns]
    if available:
        df['risk_score'] = df[available].sum(axis=1)

    # Healthy lifestyle score
    healthy_cols = [
        'Leisure Physical Activity', 'Fruit or Vegetable Intake (1+ per Day)',
        'Health Care Coverage'
    ]
    available_h = [c for c in healthy_cols if c in df.columns]
    if available_h:
        df['healthy_score'] = df[available_h].sum(axis=1)

    # BMI categories
    if 'Body Mass Index' in df.columns:
        df['bmi_cat'] = pd.cut(
            df['Body Mass Index'],
            bins=[0, 18.5, 25, 30, 100],
            labels=[0, 1, 2, 3]
        ).astype(float)

    # Age × risk interaction
    if 'Age' in df.columns and 'risk_score' in df.columns:
        df['age_x_risk'] = df['Age'] * df['risk_score']

    return df

