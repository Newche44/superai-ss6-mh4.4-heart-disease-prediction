def objective_lgb(trial):
    params = {
        'objective': 'binary',
        'metric': 'binary_logloss',
        'verbosity': -1,
        'random_state': 42,
        'scale_pos_weight': scale_pos,
        # ── Hyperparameters ที่ tune ──
        'learning_rate':     trial.suggest_float('learning_rate', 0.01, 0.1, log=True),
        'num_leaves':        trial.suggest_int('num_leaves', 31, 255),
        'min_child_samples': trial.suggest_int('min_child_samples', 10, 100),
        'feature_fraction':  trial.suggest_float('feature_fraction', 0.5, 1.0),
        'bagging_fraction':  trial.suggest_float('bagging_fraction', 0.5, 1.0),
        'bagging_freq':      trial.suggest_int('bagging_freq', 1, 10),
        'reg_alpha':         trial.suggest_float('reg_alpha', 1e-4, 10.0, log=True),
        'reg_lambda':        trial.suggest_float('reg_lambda', 1e-4, 10.0, log=True),
        'n_estimators': 1000,
    }

    # 3-fold CV (เร็วกว่า 5-fold ในช่วง tuning)
    cv3 = StratifiedKFold(n_splits=3, shuffle=True, random_state=42)
    oof = np.zeros(len(X_fe))

    for tr_idx, val_idx in cv3.split(X_fe, y):
        X_tr, X_val = X_fe.iloc[tr_idx], X_fe.iloc[val_idx]
        y_tr, y_val = y.iloc[tr_idx], y.iloc[val_idx]

        m = lgb.LGBMClassifier(**params)
        m.fit(
            X_tr, y_tr,
            eval_set=[(X_val, y_val)],
            callbacks=[lgb.early_stopping(50, verbose=False), lgb.log_evaluation(-1)]
        )
        oof[val_idx] = m.predict_proba(X_val)[:, 1]

    # optimize threshold ด้วย
    _, best_f2 = find_best_threshold(y, oof)
    return best_f2

